    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Validaciones b√°sicas (puedes a√±adir m√°s)
        if (dn.getText().trim().isEmpty() || ddh.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nombre y D√≠as/Horas son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        tutor = new Tutor(); // Crea una nueva instancia

        // NumTarjeta: Si es autoincremental en BD, no lo establezcas aqu√≠.
        // Si no, obt√©n el valor del spinner.
        tutor.setNumTarjeta((Integer) dnt.getValue()); // Asumiendo que numTarjeta no es el PK autoincremental.
                                                      // Si idPersona es el PK autoincremental, numTarjeta es un campo normal.
        tutor.setNombre(dn.getText().trim());
        tutor.setCarrera((String) cbCarreras.getSelectedItem());

        // El campo 'ddh' parece combinar d√≠as y horas. Necesitas una forma consistente de almacenarlo.
        // La entidad Tutor tiene campos separados 'dias' y 'horas'.
        // Deber√°s decidir c√≥mo parsear 'ddh.getText()' en esos dos campos o modificar la entidad/UI.
        // Ejemplo simple (necesitar√°s mejor l√≥gica de parseo):
        String diasHorasInput = ddh.getText().trim();
        // L√≥gica simple para separar:
        String[] partesDH = diasHorasInput.split(";"); // Suponiendo formato "L-M-X;08-12"
        String diasTutor = "";
        String horasTutor = "";
        if (partesDH.length > 0) diasTutor = partesDH[0].trim();
        if (partesDH.length > 1) horasTutor = partesDH[1].trim();

        tutor.setDias(diasTutor);   // Asignar los d√≠as parseados
        tutor.setHoras(horasTutor); // Asignar las horas parseadas

        try {
            cTutor.create(tutor);
            tutores.add(tutor); // A√±adir a la lista en memoria
            if (mtt != null) { // mtt es el modelo de la tabla ttutores
                mtt.fireTableDataChanged(); // Notificar a la tabla en la pesta√±a "Control"
            }
            // cargarTutores(); // Vuelve a cargar el ComboBox LTutores en la pesta√±a "Asignar Tutor"
            JOptionPane.showMessageDialog(this, "Tutor agregado exitosamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
            // limpiarCamposAgregarTutor(); // Un m√©todo para limpiar los campos de esta pesta√±a
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar tutor: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

